public class Car {

    private int speed;
    private int fuelCapacity;
    private int horsePower;
    private int baggageWeight;

    public int getSpeed() {
        return speed;
    }

    public int getFuelCapacity() {
        return fuelCapacity;
    }

    public int getHorsePower() {
        return horsePower;
    }

    public int getBaggageWeight() {
        return baggageWeight;
    }

}
